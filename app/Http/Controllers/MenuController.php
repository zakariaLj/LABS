     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
      $menu = new Menu();
      
      $menu->Name = request('Nom');
      $menu->Logo = request('logo')->store('img');
      $menu->Lien = request('lien');

      $menu->save();
      return redirect()->route('Menu.index',compact('menu'));
    }

